function applyLanguage(lang) {
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    const translation = translations[lang][key];
    if (translation) {
      // If element is an input, textarea, or button, update value/placeholder/title as appropriate
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if (el.hasAttribute('placeholder')) {
          el.setAttribute('placeholder', translation);
        }
        if (el.type === 'button' || el.type === 'submit' || el.type === 'reset') {
          el.value = translation;
        }
      } else if (el.tagName === 'BUTTON') {
        el.textContent = translation;
      } else if (el.hasAttribute('title')) {
        el.setAttribute('title', translation);
        el.textContent = translation;
      } else {
        el.textContent = translation;
      }
    } else {
      // Fallback: show the key if translation is missing
      el.textContent = key;
    }
  });
}

// Observe DOM for new [data-translate] elements and translate them
const observer = new MutationObserver(function(mutations) {
  const lang = getSavedLanguage();
  mutations.forEach(mutation => {
    mutation.addedNodes.forEach(node => {
      if (node.nodeType === 1) {
        if (node.hasAttribute && node.hasAttribute('data-translate')) {
          applyLanguage(lang);
        }
        // Also check descendants
        node.querySelectorAll && node.querySelectorAll('[data-translate]').forEach(el => {
          applyLanguage(lang);
        });
      }
    });
  });
});
observer.observe(document.body, { childList: true, subtree: true });

function setLanguage(lang) {
  localStorage.setItem('language', lang);
  applyLanguage(lang);
}

function getSavedLanguage() {
  return localStorage.getItem('language') || 'en';
}

document.addEventListener('DOMContentLoaded', function() {
  const lang = getSavedLanguage();
  applyLanguage(lang);

  const languageSelect = document.getElementById('languageSelect');
  if (languageSelect) {
    languageSelect.value = lang;
    languageSelect.addEventListener('change', function() {
      setLanguage(this.value);
    });
  }
});
